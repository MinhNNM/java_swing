            String ten=jTextField6.getText();
            int ma=Integer.parseInt(jTextField5.getText().toString());
            String loai=jComboBox2.getSelectedItem().toString();
            int solgmax=Integer.parseInt(jTextField7.getText().toString());
            tmK.setValueAt(ma, row, 0);
            tmK.setValueAt(ten, row, 1);
            tmK.setValueAt(loai, row, 2);
            tmK.setValueAt(solgmax, row, 3);
