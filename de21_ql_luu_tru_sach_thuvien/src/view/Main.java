    private void loaddata(){
        File f=new File(fS);
        if(f.exists())
            lS=IOFile.doc(fS);
        else
            lS=new ArrayList<>();
        File f1=new File(fK);
        if(f1.exists())
            lK=IOFile.doc(fK);
        else
            lK=new ArrayList<>();
        File f2=new File(fB);// chỗ này để fS
        if(f2.exists())
            lB=IOFile.doc(fB);
        else
            lB=new ArrayList<>();
    }
    private void showS(List<Sach>l){
        tmS.setRowCount(0);
        for(Sach i:l)
            tmS.addRow(i.toObject());
    }
    private void showK(List<KeSach>l){
        tmK.setRowCount(0);
        for(KeSach i:l)
            tmK.addRow(i.toObject());
    }
    private void showB(List<Bang>l){
        tmB.setRowCount(0);
        for(Bang i:l)
            tmB.addRow(i.toObject());
    }
    private void load2ma(){
        jComboBox3.removeAllItems();
        jComboBox4.removeAllItems();
        for(Sach i:lS)
            jComboBox3.addItem(i.getMa()+"");
        for(KeSach i:lK)
            jComboBox4.addItem(i.getMa()+"");
    }
    private boolean trungma(int mS, int mK){
        for(Bang i: lB){
            if(i.getS().getMa()==mS && i.getKs().getMa()==mK)
                return true;
        }
        return false;
    }
    private Sach getS(int mS){
        for(Sach i:lS)
            if(i.getMa()==mS)
                return i;
        return null;
    }
     private KeSach getK(int mK){
        for(KeSach i:lK)
            if(i.getMa()==mK)
                return i;
        return null;
    }
