    private int count(int mLX){
        int dem=0;
        for(BangPhanCong i: lDS){
            if(i.getLx().getMa()==mLX)
                dem+=i.getSoluot();
        }
        return dem;
    }
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mLX=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int mT=Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trung2ma(mLX, mT)){
            JOptionPane.showMessageDialog(this, "Trung ma");
            return;
        }
        
        int soluot;
        try {
            soluot=Integer.parseInt(jTextField7.getText().toString());
            if(count(mLX)+soluot>15){
                JOptionPane.showMessageDialog(this, "Khong duoc chay qua 15 luot");
                return;
            }
            BangPhanCong ds=new BangPhanCong(getLX(mLX), getT(mT), soluot);
            tmDS.addRow(ds.toObject());
            lDS.add(ds);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap vao so");
            return;
        }catch(SoEx e){
            JOptionPane.showMessageDialog(this, "Nhap vao so duong");
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

