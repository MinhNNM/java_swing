    private void load2ma(){
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        for(LaiXe i:lLX)
            jComboBox2.addItem(i.getMa()+"");
        for(Tuyen i:lT)
            jComboBox3.addItem(i.getMa()+"");
    }
    private void loaddata(){
        File f=new File(fLX);
        if(f.exists())
            lLX=IOFile.doc(fLX);
        else
            lLX=new ArrayList<>();
        File f1=new File(fT);
        if(f1.exists())
            lT=IOFile.doc(fT);
        else
            lT=new ArrayList<>();
        File f2=new File(fDS);
        if(f2.exists())
            lDS=IOFile.doc(fDS);
        else
            lDS=new ArrayList<>();
        
    }
    private void showLX(List<LaiXe> l){
        tmLX.setRowCount(0);
        for(LaiXe i:lLX)
            tmLX.addRow(i.toObject());
    }
    private void showT(List<Tuyen> l){
        tmT.setRowCount(0);
        for(Tuyen i:lT)
            tmT.addRow(i.toObject());
    }
    private void showDS(List<BangPhanCong> l){
        tmDS.setRowCount(0);
        for(BangPhanCong i:lDS)
            tmDS.addRow(i.toObject());
    }
    private boolean trung2ma(int mLX, int mT){
        for(BangPhanCong i:lDS){
            if(i.getLx().getMa()==mLX && i.getT().getMa()==mT)
                return true;
        }
        return false;
    }
    private LaiXe getLX(int mLX){
        for(LaiXe i:lLX)
            if(i.getMa()==mLX)
                return i;
        return null;
    }
    private Tuyen getT(int mT){
        for(Tuyen i:lT)
            if(i.getMa()==mT)
                return i;
        return null;
    }
