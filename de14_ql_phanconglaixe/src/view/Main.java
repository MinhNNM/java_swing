    private void jComboBox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox4ActionPerformed
        // TODO add your handling code here:
        if(jComboBox4.getSelectedIndex()==1){
            Collections.sort(lDS, Comparator.comparing(BangPhanCong::getTEN));
            showDS(lDS);
        }else if(jComboBox4.getSelectedIndex()==2){
            Collections.sort(lDS, new Comparator<BangPhanCong>(){
                public int compare(BangPhanCong o1, BangPhanCong o2){
                    if(o1.getSoluot()<o2.getSoluot())
                        return 1;
                    return -1;
                }
            });
            showDS(lDS);
        }
    }//GEN-LAST:event_jComboBox4ActionPerformed

