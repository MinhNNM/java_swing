    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n=lPH.size();
        if(n>0)
            PhongHoc.setSma(lPH.get(n-1).getMa()+1);
        jTextField1.setText(PhongHoc.getSma()+"");
        try {
            String ten=jTextField2.getText();
            int soghe=Integer.parseInt(jTextField3.getText().toString());
            String kieu=jComboBox1.getSelectedItem().toString();
            PhongHoc ph=new PhongHoc(ten, soghe, kieu);
            tmPH.addRow(ph.toObject());
            lPH.add(ph);
        } catch (TrongEx e) {
            JOptionPane.showMessageDialog(this, "KO duoc de trong");
            return;
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nhap vao so");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

