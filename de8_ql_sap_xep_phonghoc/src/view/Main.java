    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mPH=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int mLop=Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trungma(mPH, mLop)){
            JOptionPane.showMessageDialog(this, "trung ma");
            return;
        }
        if(getPH(mPH).getSoghe()<getLop(mLop).getSosv()){
            JOptionPane.showMessageDialog(this, "Phong ko du ghe");
            return;
        }
        int sokip;
        try {
            sokip=Integer.parseInt(jTextField7.getText().toString());
            if(trungkip(sokip)){
                JOptionPane.showMessageDialog(this, "trung kip");
                return;
        }
            BangSX ds=new BangSX(getPH(mPH), getLop(mLop), sokip);
            tmDS.addRow(ds.toObject());
            lDS.add(ds);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap vao so");
            return;
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

