    private boolean trungma(int maPh, int maLop){
        for(BangSX i: lDS){
            if(i.getPh().getMa()==maPh && i.getLop().getMa()==maLop)
                return true;
        }
        return false;
    }
    private boolean trungkip(int sokip){
        for(BangSX i: lDS){
            if(i.getKip()==sokip)
                return true;
        }
        return false;
    }
    private PhongHoc getPH(int mPH){
        for(PhongHoc i: lPH){
            if(i.getMa()==mPH)
                return i;
        }return null;
    }
    private Lop getLop(int mLop){
        for(Lop i: lLop){
            if(i.getMa()==mLop)
                return i;
        }return null;
    }
    private void load2ma(){
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        for(PhongHoc i: lPH)
            jComboBox2.addItem(i.getMa()+"");
        for(Lop i: lLop)
            jComboBox3.addItem(i.getMa()+"");
    }
    private void loaddata(){
        File f=new File(fPH);
        if(f.exists())
            lPH=IOFile.doc(fPH);
        else
            lPH=new ArrayList<>();
        File f1=new File(fPH);
        if(f1.exists())
            lLop=IOFile.doc(fLop);
        else
            lLop=new ArrayList<>();
        File f2=new File(fDS);
        if(f2.exists())
            lDS=IOFile.doc(fDS);
        else
            lDS=new ArrayList<>();
    }
    private void showPH(List<PhongHoc> l){
        tmPH.setRowCount(0);
        for(PhongHoc i:l)
            tmPH.addRow(i.toObject());
    }
    private void showLop(List<Lop> l){
        tmLop.setRowCount(0);
        for(Lop i:l)
            tmLop.addRow(i.toObject());
    }
    private void showDS(List<BangSX> l){
        tmDS.setRowCount(0);
        for(BangSX i:l)
            tmDS.addRow(i.toObject());
    }
