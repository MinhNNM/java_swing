    private boolean trungma(int mCN, int mSP){
        for(BangTinhToan i:lDS){
            if(i.getCn().getMa()==mCN && i.getSp().getMa()==mSP)
                return true;
        }
        return false;
    }
    private CongNhan getCN(int mCN){
        for(CongNhan i: lCN)
            if(i.getMa()==mCN)
                return i;
        return null;
    }
    private SanPham getSP(int mSP){
        for(SanPham i: lSP)
            if(i.getMa()==mSP)
                return i;
        return null;
    }
    private void load2ma(){
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        for(CongNhan i:lCN)
            jComboBox2.addItem(i.getMa()+"");
        for(SanPham i:lSP)
            jComboBox3.addItem(i.getMa()+"");
    }
    private void loaddata(){
        File f=new File(fCN);
        if(f.exists())
            lCN=IOFile.doc(fCN);
        else
            lCN=new ArrayList<>();
        File f1=new File(fSP);
        if(f1.exists())
            lSP=IOFile.doc(fSP);
        else
            lSP=new ArrayList<>();
        File f2=new File(fDS);
        if(f2.exists())
            lDS=IOFile.doc(fDS);
        else
            lDS=new ArrayList<>();
    }
    private void showCN(List<CongNhan> l){
        tmCN.setRowCount(0);
        for(CongNhan i:l)
            tmCN.addRow(i.toObject());
    }
    private void showSP(List<SanPham> l){
        tmSP.setRowCount(0);
        for(SanPham i:l)
            tmSP.addRow(i.toObject());
    }
    private void showDS(List<BangTinhToan> l){
        tmDS.setRowCount(0);
        for(BangTinhToan i:l)
            tmDS.addRow(i.toObject());
    }
