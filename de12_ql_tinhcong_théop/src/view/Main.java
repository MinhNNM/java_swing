    private boolean check(int mCN){
        int dem=0;
        for(BangTinhToan i:lDS){
            if(i.getCn().getMa()==mCN){
                dem++;
            }
        }
        if(dem==5)
            return true;
        return false;
    }
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mCN=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int mSP=Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trungma(mCN, mSP)){
            JOptionPane.showMessageDialog(this, "Trung Ma");
            return;
        }
        if(check(mCN)){
            JOptionPane.showMessageDialog(this, "Khong duoc lam qua 5 san pham");
            return;
        }
        try {
            int soluong=Integer.parseInt(jTextField7.getText().toString());
            BangTinhToan ds=new BangTinhToan(getCN(mCN), getSP(mSP), soluong);
            tmDS.addRow(ds.toObject());
            lDS.add(ds);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "nhap vao so");
            return;
        }catch(SoEx e){
            JOptionPane.showMessageDialog(this, "nhap vao so duong");
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

