    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n=lCN.size();
        if(n>0)
            CongNhan.setSma(lCN.get(n-1).getMa()+1);
        jTextField1.setText(CongNhan.getSma()+"");
        String ten, diachi, casx;
        try {
            ten=jTextField2.getText();
            diachi=jTextField3.getText();
            casx=jComboBox1.getSelectedItem().toString();
            CongNhan cn=new CongNhan(ten, diachi, casx);
            tmCN.addRow(cn.toObject());
            lCN.add(cn);
        } catch (TrongEx e) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

