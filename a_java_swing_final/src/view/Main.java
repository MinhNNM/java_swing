    private int count(int mNM) {
        int dem = 0;
        for (HoaDon i : lHD) {
            if (i.getNm().getMa() == mNM) {
                dem++;
            }
        }
        return dem;
    }
    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mNM = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int mLV = Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if (trungma(mNM, mLV)) {
            JOptionPane.showMessageDialog(this, "Trung ma");
            return;
        }
        try {
            int soluong = Integer.parseInt(jTextField7.getText().toString());
            if (soluong > 10) {
                JOptionPane.showMessageDialog(this, "Khong duoc mua qua 10 ve 1 loai");
                return;
            }
            if (count(mNM) == 3) {
                JOptionPane.showMessageDialog(this, "Khong duoc mua qua 3 loai ve");
                return;
            }
            HoaDon hd = new HoaDon(getNM(mNM), getLV(mLV), soluong);
            tmHD.addRow(hd.toObject());
            lHD.add(hd);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhap vao so");
            return;
        } catch (SoEx e) {
            JOptionPane.showMessageDialog(this, "Nhap vao so duong");
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

