    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        String loaive;
        double dongia;
        int ma;
        if (row >= 0 && row < jTable2.getRowCount()) {
            ma = Integer.parseInt(jTextField4.getText().toString());
            loaive = jComboBox4.getSelectedItem().toString();
            dongia = Double.parseDouble(jTextField6.getText().toString());
            tmLV.setValueAt(ma, row, 0);
            tmLV.setValueAt(loaive, row, 1);
            tmLV.setValueAt(dongia, row, 2);
            LoaiVe lv = new LoaiVe(ma, loaive, dongia);
            lLV.set(row, lv);
        } else {
            JOptionPane.showMessageDialog(this, "Chon dong de sua");
            return;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

