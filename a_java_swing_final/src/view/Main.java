    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n = lNM.size();
        if (n > 0) {
            NguoiMua.setSma(lNM.get(n - 1).getMa() + 1);
        }
        jTextField1.setText(NguoiMua.getSma() + "");
        String hoten, diachi, loai;
        try {
            hoten = jTextField2.getText();
            diachi = jTextField3.getText();
            loai = jComboBox1.getSelectedItem().toString();
            NguoiMua nm = new NguoiMua(hoten, diachi, loai);
            tmNM.addRow(nm.toObject());
            lNM.add(nm);
        } catch (TrongEx e) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

