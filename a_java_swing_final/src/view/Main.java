    private void loaddata() {
        File f = new File(fNM);
        if (f.exists()) {
            lNM = IOFile.doc(fNM);
        } else {
            lNM = new ArrayList<>();
        }
        File f1 = new File(fLV);
        if (f1.exists()) {
            lLV = IOFile.doc(fLV);
        } else {
            lLV = new ArrayList<>();
        }
        File f2 = new File(fHD);
        if (f2.exists()) {
            lHD = IOFile.doc(fHD);
        } else {
            lHD = new ArrayList<>();
        }

    }

    private void loadma() {
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        jComboBox5.removeAllItems();
        for (NguoiMua i : lNM) {
            jComboBox2.addItem(i.getMa() + "");
        }
        for (LoaiVe i : lLV) {
            jComboBox3.addItem(i.getMa() + "");
        }
        int index=0;
        List list=new ArrayList();
        for(LoaiVe i:lLV){
            if(!list.contains(i.getLoaive())){
                list.add(i.getLoaive());
                jComboBox5.addItem(i.getLoaive());
            }
        }
    }

