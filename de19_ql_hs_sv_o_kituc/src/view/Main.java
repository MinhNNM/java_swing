    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int row=jTable2.getSelectedRow();
        if(row>=0 && row<jTable2.getRowCount()){
            int ma=Integer.parseInt(jTextField6.getText().toString());
            String ten=jTextField7.getText();
            String loai=jComboBox1.getSelectedItem().toString();
            Phong p=new Phong(ma, ten, loai);
            tmP.setValueAt(ma, row, 0);
            tmP.setValueAt(ten, row, 1);
            tmP.setValueAt(loai, row, 2);
            lP.set(row, p);
        }else{
            JOptionPane.showMessageDialog(this, "Chon hang de sua");
            return;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

