    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int n=lSV.size();
        if(n>0)
            SinhVien.setSma(lSV.get(n-1).getMa()+1);
        jTextField1.setText(SinhVien.getSma()+"");
        String hoten, diachi, ngaysinh, lop;
        try {
            hoten=jTextField2.getText();
            diachi=jTextField3.getText();
            ngaysinh=jTextField4.getText();
            lop=jTextField5.getText();
            SinhVien sv=new SinhVien(hoten, diachi, ngaysinh, lop);
            tmSV.addRow(sv.toObject());
            lSV.add(sv);
        } catch (TrongEx e) {
            JOptionPane.showMessageDialog(this, "Khong duoc de trong");
            return;
        }catch(NSEx e){
            JOptionPane.showMessageDialog(this, "Ngay sinh ko dung dinh dang");
            return;
        }catch(LopEx e){
            JOptionPane.showMessageDialog(this, "Lop ko dung dinh dang cua HV");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

