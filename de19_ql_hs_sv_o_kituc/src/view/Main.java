    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int mSV=Integer.parseInt(jComboBox2.getSelectedItem().toString());
        int mP=Integer.parseInt(jComboBox3.getSelectedItem().toString());
        if(trungma(mSV, mP)){
             JOptionPane.showMessageDialog(this, "Trung ma");
            return;
        }
        if(check(mP)){
             JOptionPane.showMessageDialog(this, "Phong da day");
            return;
        }
        try {
            String ngaybd=jTextField8.getText();
            DanhSach ds=new DanhSach(getSV(mSV), getP(mP), ngaybd);
            tmDS.addRow(ds.toObject());
            lDS.add(ds);
        } catch (NSEx e) {
             JOptionPane.showMessageDialog(this, "Ngay ko dung dinh dang");
            return;
        }
    }//GEN-LAST:event_jButton7ActionPerformed

